def persegi(s):
    luas = s * s
    keliling = 4 * s
    print(f"Luas\t\t: {luas}")
    print(f"Keliling\t: {keliling}")

def persegiPanjang(l,p):
    luas = l * p
    keliling = 2 * (l*p)
    print(f"Luas\t\t: {luas}")
    print(f"Keliling\t: {keliling}")

def lingkaran(r):
    luas = 3.14 * r * r
    keliling = 2 * 3.14 * r
    print(f"Luas\t\t: {luas}")
    print(f"Keliling\t: {keliling}")
